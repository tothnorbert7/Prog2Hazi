public class Pont {
    private double x;
    private double y;
    
    public Pont(double x, double y)
    {
        this.x=x;
        this.y=y;
    }
    
    public double getX()
    {
        return this.x;
    }
    
    public void setX(double a)
    {
        this.x=a;
    }
    
    public double getY()
    {
        return this.y;
    }
    
    public void setY(double a)
    {
        this.y=a;
    }
    
}
